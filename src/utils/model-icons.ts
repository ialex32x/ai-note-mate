/** A recursive SVG element definition for use inside {@code <defs>}. */
export interface SvgDefElement {
    readonly tag: string;
    readonly attr: Record<string, string>;
    readonly children?: readonly SvgDefElement[];
}

/**
 * Structured definition of a vendor logo icon.
 *
 * Each icon is defined by its rendering mode (`stroke` for outlined icons
 * or `fill` for solid icons), a brand colour, and an array of SVG path `d`
 * attribute strings.  All icons use a 24×24 viewBox.
 *
 * Multi-colour icons can supply per-path attribute overrides via
 * {@link pathAttrs} and gradient/filter definitions via {@link defs}.
 */
export interface ModelIconDef {
    readonly type: 'fill' | 'stroke';
    /** Brand colour applied directly as the fill or stroke value. */
    readonly color: string;
    readonly paths: readonly string[];
    /** Optional per-path attribute overrides (e.g. fill for individual paths). */
    readonly pathAttrs?: readonly (Record<string, string> | null)[];
    /** Optional SVG {@code <defs>} children for gradients, filters, etc. */
    readonly defs?: readonly SvgDefElement[];
}

